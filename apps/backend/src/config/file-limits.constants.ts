// Единый источник правды для лимитов размера файлов. Дефолтные значения (в МБ)
// живут здесь; env-переменные их переопределяют в рантайме (см. env.schema.ts),
// а чистые функции без DI (напр. page-validator) берут дефолт прямо отсюда.
// Нигде в коде не должно оставаться литералов вида `N * 1024 * 1024`.

export const BYTES_PER_MB = 1024 * 1024;

/**
 * Лимит загружаемого файла OpenAPI-спецификации. Ограничен так, чтобы результат
 * импорта гарантированно помещался в документ MongoDB (жёсткий потолок 16 МБ):
 * полный развёрнутый снапшот больше не сохраняется, в документ идут только
 * endpoints и их схемы, поэтому 15 МБ исходного файла безопасны.
 */
export const DEFAULT_SPEC_FILE_MAX_MB = 15;

/** Порог single-upload; выше него включается чанковая загрузка. */
export const DEFAULT_SINGLE_UPLOAD_MAX_MB = 10;

/** Лимит размера ответа при загрузке спецификации по URL (SSRF-guard). */
export const DEFAULT_SPEC_URL_FETCH_MAX_MB = 10;

/** Размер одной части при чанковой загрузке. */
export const DEFAULT_UPLOAD_PART_SIZE_MB = 5;

/**
 * Лимит файлового ответа внешнего API, сохраняемого воркером в S3 как артефакт
 * запуска. Тело буферизуется в памяти воркера целиком, поэтому потолок жёсткий.
 */
export const DEFAULT_RUN_ARTIFACT_MAX_MB = 50;

export function mbToBytes(mb: number): number {
  return mb * BYTES_PER_MB;
}
