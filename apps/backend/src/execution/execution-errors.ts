/**
 * Детерминированный сбой шага (недоступный endpoint, ошибка стороннего API,
 * приложение без базового URL, фильтр массива без совпадений). Повторная
 * доставка сообщения такой сбой не лечит — она лишь трижды повторит то же
 * падение, поэтому `executeRun` фиксирует `failed` и ПОДТВЕРЖДАЕТ сообщение.
 * Всё, что НЕ является StepExecutionError (например, недоступность MongoDB),
 * пробрасывается наружу — там retry уместен.
 */
export class StepExecutionError extends Error {}

/** Отмена — не ошибка: статус уже выставлен, шаги просто прекращаем. */
export class RunCancelledError extends Error {}

/**
 * Шаг ждёт ввода пользователя (страница или добор ручных значений). НЕ ошибка и
 * НЕ отмена: `executeRun` завершается штатно и ПОДТВЕРЖДАЕТ сообщение SQS, чтобы
 * ожидание не держало обработчик занятым (иначе одна непросабмиченная страница
 * морозит весь консьюмер). Продолжение приезжает отдельным сообщением, которое
 * `ExecutionService` кладёт в очередь при submit'е.
 */
export class RunPausedError extends Error {}
